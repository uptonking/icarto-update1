namespace iCarto.common.consts
{
    public enum DirectionEnum : int
    {
        /// <summary>
        /// 无方向
        /// </summary>
        NONE,
        /// <summary>
        /// 从上往下
        /// </summary>
        UP_TO_DOWN,
        /// <summary>
        /// 从下往上
        /// </summary>
        DOWN_TO_UP,
        /// <summary>
        /// 从左往右
        /// </summary>
        LEFT_TO_RIGHT,
        /// <summary>
        /// 从右往左
        /// </summary>
        RIGHT_TO_LEFT,
        /// <summary>
        /// 从左上往右下
        /// </summary>
        LEFT_UP_TO_RIGHT_DOWN,
        /// <summary>
        /// 从右下往左上
        /// </summary>
        RIGHT_DOWN_TO_LEFT_UP,
        /// <summary>
        /// 从左下往右上
        /// </summary>
        LEFT_DOWN_TO_RIGHT_UP,
        /// <summary>
        /// 从右上往左下
        /// </summary>
        RIGHT_UP_TO_LEFT_DOWN
    }
}
